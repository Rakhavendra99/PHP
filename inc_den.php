<h2> Increment / Decrement operator</h2>
<?php
	$x =10;
	echo $x."<br>";
	
	echo $x++."<br>"; //post increment
	echo $x."<br>";
	
	echo ++$x."<br>"; // pre increment
	echo $x."<br>";
	
	echo $x--."<br>"; //post decrement
	echo $x."<br>";
	
	echo --$x."<br>"; //pre decrement 
	echo $x."<br>";



?>
